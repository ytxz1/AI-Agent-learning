"""
天气工具：获取城市天气信息

模拟天气数据，实际项目中可以接入真实的天气 API（如和风天气、OpenWeatherMap）
"""


def get_weather(city: str) -> str:
    """
    获取指定城市的天气信息

    参数:
        city: 城市名称，如 "北京"、"上海"

    返回:
        天气信息字符串
    """
    # 模拟天气数据（实际项目中调用真实 API）
    weather_data = {
        "北京": "25°C，晴天，微风",
        "上海": "28°C，多云，东南风3级",
        "广州": "32°C，雷阵雨，湿度85%",
        "深圳": "30°C，小雨，阵风5级",
        "杭州": "26°C，阴天，西风2级",
        "成都": "24°C，多云转晴，无持续风向",
        "武汉": "27°C，晴转多云，北风3级",
        "南京": "25°C，多云，东北风2级",
        "西安": "23°C，晴天，微风",
        "重庆": "29°C，阴天，湿度较大",
    }

    # 模糊匹配
    for key, value in weather_data.items():
        if key in city:
            return f"{city}天气：{value}"

    return f"未找到 {city} 的天气信息，目前支持：{', '.join(weather_data.keys())}"
