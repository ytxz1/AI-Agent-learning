"""
Zero-Shot Prompting (零样本提示) 示例
===================================

零样本提示是指在不提供任何示例的情况下，直接让大语言模型完成任务。
模型依靠预训练时学到的知识来理解和执行任务。
"""

from openai import OpenAI
from dotenv import load_dotenv
import os

load_dotenv()


# ============================================================
# 1. 基础配置
# ============================================================
# 初始化 OpenAI 客户端
# 这里使用 DeepSeek 作为后端，也可以替换为 OpenAI 或其他兼容的 API
client = OpenAI(
    api_key=os.getenv('OPENAI_API_KEY'),  # 从 .env 文件读取 API 密钥
    base_url='https://api.deepseek.com'               # DeepSeek API 地址
)


# ============================================================
# 2. 零样本提示示例
# ============================================================
# 直接调用 API，不提供任何示例
# 模型将根据任务描述直接给出回答
response = client.chat.completions.create(
    model="deepseek-chat",  # 使用的模型名称
    messages=[
        # system 消息：定义模型的角色和行为
        {"role": "system", "content": "You are a helpful assistant."},
        # user 消息：直接提出问题或任务
        # 这里是零样本的核心 - 不提供任何示例，直接提问
        {"role": "user", "content": "What is the capital of France?"}
    ]
)

# 输出模型的回答
print(response.choices[0].message.content)


# ============================================================
# 详细解释
# ============================================================
"""
什么是零样本提示 (Zero-Shot Prompting)?
=======================================

1. 核心概念
   ----------
   零样本提示是最简单的提示方式。用户直接描述任务需求，
   不提供任何示例，完全依赖模型预训练时获得的知识来完成任务。

   在本示例中：
   - system 消息定义了模型的角色（有帮助的助手）
   - user 消息直接提出问题："法国的首都是什么？"
   - 没有提供任何示例，模型直接回答

2. 工作原理
   ----------
   - 模型在大规模文本数据上预训练，学习了语言的通用模式
   - 当收到任务描述时，模型基于已学习的知识进行推理
   - 无需额外示例，模型能够理解并执行任务
   - 对于事实性问题（如首都、数学公式等），模型通常能直接回答

3. messages 参数解析
   ------------------
   - role: "system"  -> 系统消息，定义模型的行为和角色
   - role: "user"    -> 用户消息，提出问题或任务
   - role: "assistant" -> 助手消息（用于少样本提示中的示例回答）

4. 适用场景
   ----------
   - 简单直接的事实性问题（如本示例）
   - 分类、翻译、摘要等任务
   - 任务描述清晰明确的场景
   - 模型对任务类型有一定先验知识

5. 优点
   ------
   - 简单易用，无需准备示例
   - 节省 token 消耗（不需要示例占用上下文）
   - 适合快速原型验证

6. 局限性
   --------
   - 对于复杂或专业任务效果可能不佳
   - 模型可能误解任务意图
   - 输出格式可能不够稳定

7. 与少样本提示 (Few-Shot) 的区别
   --------------------------------
   - 零样本：不提供示例，直接描述任务（本文件）
   - 少样本：提供 1-5 个示例，帮助模型理解任务模式（见 few_shot.py）

8. 扩展示例
   ----------
   # 情感分析（零样本）
   messages = [
       {"role": "user", "content": "判断以下句子的情感：'这部电影太棒了！'"}
   ]

   # 翻译任务（零样本）
   messages = [
       {"role": "user", "content": "将以下中文翻译成英文：'今天天气真好'"}
   ]

   # 代码生成（零样本）
   messages = [
       {"role": "user", "content": "用 Python 写一个函数，计算斐波那契数列"}
   ]

9. 最佳实践
   ----------
   - 任务描述要清晰明确
   - 指定输出格式（如 JSON、列表等）
   - 添加约束条件（如字数限制、语言要求等）
   - 对于复杂任务，考虑使用少样本提示或思维链提示
"""
