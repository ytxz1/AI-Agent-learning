"""
English Teacher (英语老师) 示例
==============================

一个交互式英语学习助手，能够：
1. 纠正语法错误
2. 给出更自然的表达
3. 用中文解释错误原因
4. 鼓励学生继续学习

使用流式输出实现"打字机"效果，提升用户体验。
"""

from openai import OpenAI
from dotenv import load_dotenv
import os

load_dotenv()


# ============================================================
# 1. 基础配置
# ============================================================
# 初始化 OpenAI 客户端
client = OpenAI(
    api_key=os.getenv('OPENAI_API_KEY'),  # 从 .env 文件读取 API 密钥
    base_url='https://api.deepseek.com'               # DeepSeek API 地址
)


# ============================================================
# 2. 系统提示 - 定义英语老师的角色
# ============================================================
# 使用多行字符串定义详细的系统提示
# 这会指导模型以特定的方式回答问题
messages = [
    {
        "role": "system",  # 系统消息：定义模型角色
        "content": """
你是专业英语老师。

你的任务：
1. 纠正语法错误
2. 给出更自然表达
3. 用中文解释错误原因
4. 鼓励学生
"""
    }
]


# ============================================================
# 3. 交互式对话循环
# ============================================================
# while True 创建无限循环，让用户可以持续对话
while True:

    # 获取用户输入
    user_input = input("\n你:")

    # 检查退出命令
    if user_input == "quit":
        break

    # 将用户输入添加到消息历史中
    messages.append({
        "role": "user",
        "content": user_input
    })

    # 调用 API - 流式输出
    response = client.chat.completions.create(
        model="deepseek-chat",  # 使用的模型名称
        messages=messages,       # 完整的消息历史
        stream=True              # 启用流式输出
    )

    # 流式输出：逐个 chunk 打印，并拼接完整回复
    print("\n老师：")
    reply = ""  # 用于存储完整的回复
    for chunk in response:
        # 检查 chunk 是否包含有效内容
        if chunk.choices[0].delta.content is not None:
            content = chunk.choices[0].delta.content
            print(content, end="", flush=True)  # 逐字打印
            reply += content  # 拼接完整回复

    # 打印完成后换行
    print()

    # 将助手的回复添加到消息历史中
    # 这样模型就能记住之前的对话内容
    messages.append({
        "role": "assistant",
        "content": reply
    })


