"""
System Prompt (系统提示) 示例
============================

系统提示用于定义模型的角色、行为和回答风格。
它是指导模型如何回答用户问题的重要工具。
"""

from openai import OpenAI
from dotenv import load_dotenv
import os

load_dotenv()


# ============================================================
# 1. 基础配置
# ============================================================
# 初始化 OpenAI 客户端
client = OpenAI(
    api_key=os.getenv('OPENAI_API_KEY'),  # 从 .env 文件读取 API 密钥
    base_url='https://api.deepseek.com'               # DeepSeek API 地址
)


# ============================================================
# 2. 系统提示示例
# ============================================================
# 系统提示的核心：通过 system 消息定义模型的角色和行为
# 这会影响模型回答的风格、语气和内容
messages = [
    {
        "role": "system",  # 系统消息：定义模型的角色
        "content": "你是一个专业且耐心的 Python 导师"  # 角色设定
    },
    {
        "role": "user",  # 用户消息：提出问题
        "content": "什么是装饰器？"  # 用户的问题
    }
]

# 调用 API - 流式输出
# stream=True 启用流式输出，模型会逐步返回结果
response = client.chat.completions.create(
    model="deepseek-chat",  # 使用的模型名称
    messages=messages,       # 包含系统提示的消息列表
    stream=True              # 启用流式输出
)

# 流式输出：逐个 chunk 打印，实现"打字机"效果
# 每个 chunk 包含一小段文本，逐步拼接成完整回答
for chunk in response:
    # 检查 chunk 是否包含有效内容
    if chunk.choices[0].delta.content is not None:
        # end="" 表示不换行，flush=True 表示立即刷新输出
        print(chunk.choices[0].delta.content, end="", flush=True)

# 打印完成后换行
print()


