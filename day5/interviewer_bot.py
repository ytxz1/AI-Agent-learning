"""
Interviewer Bot (面试官机器人) 示例
==================================

一个模拟大厂 AI 面试的交互式聊天机器人，能够：
1. 每次只问一个问题
2. 根据用户回答继续追问
3. 回答结束后给评价
4. 问题难度逐渐增加

这是一个多轮对话的典型应用，展示了如何构建交互式 AI 应用。
"""

from openai import OpenAI
from dotenv import load_dotenv
import os

load_dotenv()


# ============================================================
# 1. 基础配置
# ============================================================
# 初始化 OpenAI 客户端
client = OpenAI(
    api_key=os.getenv('OPENAI_API_KEY'),  # 从 .env 文件读取 API 密钥
    base_url='https://api.deepseek.com'               # DeepSeek API 地址
)


# ============================================================
# 2. 系统提示 - 定义面试官的角色
# ============================================================
# 使用多行字符串定义详细的系统提示
# 这会指导模型以特定的方式进行面试
messages = [
    {
        "role": "system",  # 系统消息：定义模型角色
        "content": """
你是大厂 AI 面试官。

规则：
1. 每次只问一个问题
2. 根据用户回答继续追问
3. 回答结束后给评价
4. 问题难度逐渐增加
"""
    }
]


# ============================================================
# 3. 初始化面试 - 用户输入"开始面试"
# ============================================================
# 将用户输入添加到消息历史中
messages.append({
    "role": "user",  # 用户消息
    "content": "开始面试"
})


# ============================================================
# 4. 面试循环
# ============================================================
# while True 创建无限循环，让用户可以持续面试
while True:

    # 调用 API 获取面试官的回复
    response = client.chat.completions.create(
        model="deepseek-chat",  # 使用的模型名称
        messages=messages       # 完整的消息历史
    )

    # 获取面试官的回复
    reply = response.choices[0].message.content

    # 打印面试官的问题
    print("\n面试官：")
    print(reply)

    # 将面试官的回复添加到消息历史中
    # 这样模型就能记住之前的面试内容
    messages.append({
        "role": "assistant",
        "content": reply
    })

    # 获取用户输入
    user_input = input("\n你：")

    # 检查退出命令
    if user_input == "quit":
        break

    # 将用户输入添加到消息历史中
    messages.append({
        "role": "user",
        "content": user_input
    })


