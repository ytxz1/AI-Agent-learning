from openai import OpenAI
from dotenv import load_dotenv
import os

load_dotenv()                          # 导入 OpenAI SDK，DeepSeek 兼容此接口
client = OpenAI(                                    # 创建 API 客户端实例
    api_key=os.getenv('OPENAI_API_KEY'),  # 从 .env 文件读取 API 密钥
    base_url='https://api.deepseek.com'             # DeepSeek 的 API 地址
)                                                   #
prompt = "请用中文介绍一下什么是温度。"              # 发给模型的问题
temps = [0, 0.5, 1.2]                              # 要测试的三个温度值

for i in temps:                                     # 遍历每个温度值
    print(f"温度设置为 {i} 时的回答：")               # 打印当前温度
    response = client.chat.completions.create(      # 调用聊天接口
        model="deepseek-chat",                      # 使用 DeepSeek 聊天模型
        messages=[                                  # 对话消息列表
            {"role": "user", "content": prompt}     # 用户消息
        ],                                          #
        temperature=i,                              # 设置温度参数
    )                                               #
    print(response.choices[0].message.content)      # 打印模型回答
